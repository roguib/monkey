package org.monkey.ws;

public class RawProgram {
    private String rawProgram;

    public RawProgram() {
    }

    public RawProgram(final String rawProgram) {
        this.rawProgram = rawProgram;
    }

    public String getRawProgram() {
        return this.rawProgram;
    }

    public void setRawProgram(final String rawProgram) {
        this.rawProgram = rawProgram;
    }
}
